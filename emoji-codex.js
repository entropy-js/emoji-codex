var Codex = function() {};

Codex.prototype.translate = function(emoji_str) {

  //Match every shortcode in the string: 
  var index_x, index_y;

  for (var i = 0; i < emoji_str.length; i++) {
    index_x = -1;
    index_y = -1;

    if (emoji_str.charAt(i) === ':') {
      index_x = i;
      for (var k = i + 1; k < emoji_str.length; k++) {
        if (emoji_str.charAt(k) === ':') {
          index_y = k+1;
          break;
        }
      }

      if (index_x > -1 && index_y > -1) {
        var substr = emoji_str.substring(index_x, index_y);

        //See if emoji exists and if so, replace it
        
        var emoji = Codex.prototype[substr];
        var unicode = ""; // = String.fromCodePoint(emoji);
        if (emoji.isArray()) {
           for (var i = 0; i < emoji.length; i++) {
               unicode += String.fromCodePoint(emoji[i]);
           }
        }  else {
            unicode = String.fromCodePoint(emoji);
        }
          
        
        if (emoji !== undefined) {
          emoji_str = emoji_str.replace(substr, unicode);
        }
      }
    }
  }
  return emoji_str;
};

